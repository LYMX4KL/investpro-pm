ALTER TABLE companies ENABLE ROW LEVEL SECURITY;
ALTER TABLE agent_emails ENABLE ROW LEVEL SECURITY;

-- Companies — broker/compliance/admin_onsite read all, broker can write
DROP POLICY IF EXISTS companies_managers_read ON companies;
CREATE POLICY companies_managers_read ON companies
  FOR SELECT TO authenticated
  USING (current_user_role() IN ('broker', 'compliance', 'admin_onsite'));

DROP POLICY IF EXISTS companies_broker_write ON companies;
CREATE POLICY companies_broker_write ON companies
  FOR ALL TO authenticated
  USING (current_user_role() = 'broker')
  WITH CHECK (current_user_role() = 'broker');

-- agent_emails — managers see all, agents see only their own
DROP POLICY IF EXISTS agent_emails_managers_all ON agent_emails;
CREATE POLICY agent_emails_managers_all ON agent_emails
  FOR ALL TO authenticated
  USING (current_user_role() IN ('broker', 'compliance', 'admin_onsite'))
  WITH CHECK (current_user_role() IN ('broker', 'compliance', 'admin_onsite'));

DROP POLICY IF EXISTS agent_emails_self_read ON agent_emails;
CREATE POLICY agent_emails_self_read ON agent_emails
  FOR SELECT TO authenticated
  USING (profile_id = auth.uid());



-- ----------------------------------------------------------------
-- 6. Helper function: pick a non-conflicting local_part
-- ----------------------------------------------------------------
-- Generates 'first.last' with a numeric suffix on collision.
-- Called by the platform when provisioning an email.
CREATE OR REPLACE FUNCTION agent_email_pick_local_part(
  p_first_name TEXT,
  p_last_name  TEXT,
  p_domain     TEXT
) RETURNS TEXT LANGUAGE plpgsql AS $$
DECLARE
  v_base    TEXT;
  v_n       INT := 1;
  v_test    TEXT;
  v_full    TEXT;
BEGIN
  -- Lowercase, strip non-alphanumeric except dot/hyphen, dot between first+last
  v_base := lower(
    regexp_replace(coalesce(p_first_name, ''), '[^a-zA-Z]', '', 'g')
    || '.'
    || regexp_replace(coalesce(p_last_name, ''), '[^a-zA-Z]', '', 'g')
  );
  IF v_base = '.' OR length(v_base) < 3 THEN
    RAISE EXCEPTION 'cannot derive a usable local-part from "%" / "%"', p_first_name, p_last_name;
  END IF;

  v_test := v_base;
  v_full := v_test || '@' || p_domain;
  WHILE EXISTS (SELECT 1 FROM agent_emails WHERE LOWER(full_email) = LOWER(v_full)) LOOP
    v_n := v_n + 1;
    v_test := v_base || '.' || v_n::text;
    v_full := v_test || '@' || p_domain;
    IF v_n > 99 THEN
      RAISE EXCEPTION 'too many collisions for "%"', v_base;
    END IF;
  END LOOP;
  RETURN v_test;
END;
$$;

GRANT EXECUTE ON FUNCTION agent_email_pick_local_part(TEXT, TEXT, TEXT) TO authenticated;


